package com.srb.bean;

public class ExamNameList {

	private Long ExamNameId;
	private String ExamName;
	private String AcadamicsYear;
	private String DivisionName;
	private String ClassName;
	private String subjectName;
	private String TotalMarks;
	private String passingMarks;
	public Long getExamNameId() {
		return ExamNameId;
	}
	public void setExamNameId(Long examNameId) {
		ExamNameId = examNameId;
	}
	public String getExamName() {
		return ExamName;
	}
	public void setExamName(String examName) {
		ExamName = examName;
	}
	public String getAcadamicsYear() {
		return AcadamicsYear;
	}
	public void setAcadamicsYear(String acadamicsYear) {
		AcadamicsYear = acadamicsYear;
	}
	public String getDivisionName() {
		return DivisionName;
	}
	public void setDivisionName(String divisionName) {
		DivisionName = divisionName;
	}
	public String getClassName() {
		return ClassName;
	}
	public void setClassName(String className) {
		ClassName = className;
	}
	public String getSubjectName() {
		return subjectName;
	}
	public void setSubjectName(String subjectName) {
		this.subjectName = subjectName;
	}
	public String getTotalMarks() {
		return TotalMarks;
	}
	public void setTotalMarks(String totalMarks) {
		TotalMarks = totalMarks;
	}
	public String getPassingMarks() {
		return passingMarks;
	}
	public void setPassingMarks(String passingMarks) {
		this.passingMarks = passingMarks;
	}
	
	
}
